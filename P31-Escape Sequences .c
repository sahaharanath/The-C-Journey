#include <stdio.h>

int main() {
    // \n: new line
    printf("Hello\nWorld\n");

    // \t: horizontal tab
    printf("Hello\tWorld\n");

    // \r: carriage return
    printf("Hello\rWorld\n");

    // \a: audible bell
    printf("Hello\aWorld\n");

    // \': print single quotation
    printf("Hello \'World\'\n");

    // \": print double quotation
    printf("Hello \"World\"\n");

    // \?: question mark sequence
    printf("Hello\?World\n");

    // \\: backslash
    printf("Hello\\World\n");

    // \f: form feed
    printf("Hello\fWorld\n");

    // \v: vertical tab
    printf("Hello\vWorld\n");

    return 0;
}
